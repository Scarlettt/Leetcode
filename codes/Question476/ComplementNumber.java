package leetcode;

/***
给定一个正整数，输出它的补数。补数是对该数的二进制表示取反。
注意:
给定的整数保证在32位带符号整数的范围内。
你可以假定二进制数不包含前导零位。
476
***/

public class ComplementNumber {
	public int findComplement(int num) {
        int temp = num;
        int i = 0;
        while(temp > 0){
            temp = temp >> 1;
            i = (i << 1) + 1;
        }
        return num^i;
    }
}
